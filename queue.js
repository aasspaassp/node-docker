function setAdds() {
    $("#MainPart_divProgressbarBox").after(`
    <div id="CustomAdds">
      <iframe src="https://ads.boletia.com">
      </iframe>
    </div>
  `);
}

function getResponsiveBanner(event) {
    const size = $(window).width();
    if (size < 600) {
        return event.banners.mobile;
    }

    return event.banners.desktop;
}

function setResponsiveBanner(event) {
    const dataElement = {
        id: "CustomBanner",
        style: `background-image:url("${getResponsiveBanner(event)}");`,
    };
    const $div = $("<div>", dataElement);
    $("#headertext").before($div);

    $(window).on("resize", function () {
        $("#CustomBanner").css(
            "background-image",
            `url("${getResponsiveBanner(event)}")`
        );
    });
}

